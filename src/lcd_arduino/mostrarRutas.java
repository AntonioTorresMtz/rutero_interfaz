    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        int row = tblRutas.getSelectedRow();
        String fila = new String();
        for(int i=1; i<8; i++){
            String tmp = tblRutas.getValueAt(row, i).toString();
            if(!tmp.equals(""))
                fila += " / "+tmp;
        } 
        
        //fila = tblRutas.getValueAt(row, row).toString();
        try {
            Arduino.sendData(" "+ fila);
        } catch (Exception ex) {
            Logger.getLogger(nuevaRuta.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    
    }//GEN-LAST:event_btnActualizarActionPerformed

