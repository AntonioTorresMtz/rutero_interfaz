    public void listar() {
        modelo = (DefaultTableModel) tblRutas.getModel();
        Object[] ob = new Object[8];
        for (int i = 0; i < listaRutas.size(); i++) {
            ob[0] = listaRutas.get(i).getRuta();
            String rutaa[] = listaRutas.get(i).getLugares(i);
            for (int j = 0; j < rutaa.length; j++) {
                ob[j + 1] = rutaa[j];
            }
            modelo.addRow(ob);
        }
        tblRutas.setModel(modelo);

    }

    public void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {//Siempre va a eliminar la fila cero
            modelo.removeRow(i);
            i = i - 1;
        }
    }

